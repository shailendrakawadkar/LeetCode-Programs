class Solution
{
    public:
    //Function to find the maximum occurring character in a string.
    char getMaxOccuringChar(string str)
    {
        vector<int> v(26, 0);
        for(auto it : str) ++v[it - 97];
        
        int index = max_element(v.begin(), v.end()) - v.begin();
        
        return 97 + index;
    }

};

